package com.BasicJava;

public class SingleArray {

	public static void main(String[] args) 
	{
		int a[]=new int [3];
		a[0]=30;
		a[1]=40;
		a[2]=50;
		System.out.println(a[0]);
		System.out.println(a[1]);
		System.out.println(a[2]);
		System.out.println("*************************************");
		for(int b=0;b<a.length;b++)
		{
			System.out.println(a[b]);

	}}

}
